#!/usr/bin/env node
/**
 * Strict-TypeScript baseline gate (ratchet).
 *
 * Runs `tsc --noEmit -p tsconfig.strict.json`, counts the strict
 * errors per source file, and compares against a committed baseline
 * snapshot. The baseline is a ratchet: a file's allowed error count
 * may only go DOWN. Any increase, or a brand-new file with errors,
 * fails the gate. Files that reached zero are dropped from the
 * baseline and must stay clean.
 *
 * Usage:
 *   node scripts/check-strict-ts.mjs           # check mode (CI)
 *   node scripts/check-strict-ts.mjs --update  # regenerate baseline
 *
 * Exit codes:
 *   0  no regressions
 *   1  strict errors increased / new file regressed (CI must fail)
 *   2  internal error (tsc missing, baseline missing, ...)
 *
 * The baseline lives at docs-internal/strict-ts-baseline.json.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { spawnSync } from "node:child_process";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, "..");
const baselinePath = resolve(repoRoot, "docs-internal", "strict-ts-baseline.json");

// Matches lines like: src/Module/Shop.ts(123,45): error TS7053: ...
// Covers both production (src/) and test (spec/) sources so the gate
// matches `npm run typecheck:strict` exactly.
const ERROR_RE = /^((?:src|spec)\/[^(]+)\((\d+),(\d+)\): error (TS\d+):/;

function runStrictTsc() {
  // shell:true is required on Windows so the npx.cmd shim resolves;
  // args are all fixed literals (no user input) so quoting is moot.
  const res = spawnSync(
    "npx tsc --noEmit -p tsconfig.strict.json",
    { cwd: repoRoot, encoding: "utf8", maxBuffer: 64 * 1024 * 1024, shell: true },
  );
  if (res.error) throw res.error;
  // tsc exits 1 when there are type errors; that is expected here.
  // Only a missing binary / spawn failure is fatal (res.error above).
  const out = `${res.stdout || ""}\n${res.stderr || ""}`;
  return out;
}

function countPerFile(output) {
  const counts = {};
  for (const line of output.split(/\r?\n/)) {
    const m = ERROR_RE.exec(line.trim());
    if (!m) continue;
    const file = m[1];
    counts[file] = (counts[file] || 0) + 1;
  }
  return counts;
}

function totalOf(counts) {
  return Object.values(counts).reduce((a, b) => a + b, 0);
}

function loadBaseline() {
  try {
    const raw = readFileSync(baselinePath, "utf8");
    const parsed = JSON.parse(raw);
    if (typeof parsed.files !== "object" || parsed.files === null) {
      throw new Error("baseline.files is not an object");
    }
    return parsed.files;
  } catch (err) {
    if (err.code === "ENOENT") return null;
    throw err;
  }
}

function writeBaseline(counts) {
  const files = {};
  for (const file of Object.keys(counts).sort()) {
    files[file] = counts[file];
  }
  const payload = {
    note: "Generated by scripts/check-strict-ts.mjs --update. Do not edit by hand. Ratchet: per-file counts may only decrease.",
    generatedAt: new Date().toISOString(),
    total: totalOf(counts),
    fileCount: Object.keys(files).length,
    files,
  };
  writeFileSync(baselinePath, JSON.stringify(payload, null, 2) + "\n", "utf8");
}

function main() {
  const update = process.argv.includes("--update");

  let output;
  try {
    output = runStrictTsc();
  } catch (err) {
    console.error(`[strict-ts] ${err.message}`);
    process.exit(2);
  }

  const current = countPerFile(output);

  if (update) {
    writeBaseline(current);
    console.log(`[strict-ts] baseline updated: ${totalOf(current)} errors in ${Object.keys(current).length} files -> ${baselinePath}`);
    process.exit(0);
  }

  const baseline = loadBaseline();
  if (baseline === null) {
    console.error(`[strict-ts] baseline not found at ${baselinePath}. Run with --update to create it.`);
    process.exit(2);
  }

  const regressed = [];
  const improved = [];
  for (const file of new Set([...Object.keys(current), ...Object.keys(baseline)])) {
    const now = current[file] || 0;
    const was = baseline[file] || 0;
    if (now > was) regressed.push({ file, was, now });
    else if (now < was) improved.push({ file, was, now });
  }

  regressed.sort((a, b) => a.file.localeCompare(b.file));
  improved.sort((a, b) => a.file.localeCompare(b.file));

  if (improved.length > 0) {
    console.log(`[strict-ts] ${improved.length} file(s) improved (good, run --update to refresh baseline):`);
    for (const r of improved) console.log(`  - ${r.file}: ${r.was} -> ${r.now}`);
  }

  if (regressed.length > 0) {
    console.error(`[strict-ts] FAIL: ${regressed.length} file(s) regressed:`);
    for (const r of regressed) console.error(`  + ${r.file}: ${r.was} -> ${r.now}`);
    console.error(`[strict-ts] Fix the new strict errors, or if intentional refresh the baseline with:`);
    console.error(`  node scripts/check-strict-ts.mjs --update`);
    process.exit(1);
  }

  console.log(`[strict-ts] OK: ${totalOf(current)} errors, no per-file regressions vs baseline.`);
  process.exit(0);
}

main();
